export function getWorksheetBySlug(slug: string) {
  return worksheets.find((w) => w.slug === slug);
}

export function getWorksheetsByCategory(categorySlug: string) {
  return worksheets.filter(
    (w) => w.categorySlug === categorySlug || w.category.toLowerCase().replace(/\s+/g, "-") === categorySlug
  );
}

export function searchWorksheets(q: string) {
  const term = q.toLowerCase();
  return worksheets.filter(
    (w) =>
      w.title.toLowerCase().includes(term) ||
      w.description.toLowerCase().includes(term) ||
      w.tags.some((t) => t.includes(term)) ||
      w.category.toLowerCase().includes(term)
  );
}

export function getRelatedWorksheets(current: Worksheet, limit = 4) {
  return worksheets
    .filter((w) => w.id !== current.id && (w.categorySlug === current.categorySlug || w.tags.some((t) => current.tags.includes(t))))
    .slice(0, limit);
}
